package com.pingpong.ui.config;

public class UrlConfig {

    String servicecount;
    String servicegame;
    String serviceplayer;

    public String getServicecount() {
        return servicecount;
    }

    public void setServicecount(String servicecount) {
        this.servicecount = servicecount;
    }

    public String getServicegame() {
        return servicegame;
    }

    public void setServicegame(String servicegame) {
        this.servicegame = servicegame;
    }

    public String getServiceplayer() {
        return serviceplayer;
    }

    public void setServiceplayer(String serviceplayer) {
        this.serviceplayer = serviceplayer;
    }
}
